import turtle
t = turtle.Turtle()
t.pensize(2)
count = 0
while count < 5:
    t.forward(100)
    t.right(90)
    t.forward(200)
    t.right(90)
    t.forward(200)
    t.right(90)
    t.forward(200)
    t.right(90)
    t.forward(100)
    t.right(72)
    count += 1 



turtle.done()
turtle.mainloop()


